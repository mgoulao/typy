def test_result_errors_and_warnings_split():
    diags = [
        VerificationDiagnostic(
            code="VFY_E001", check="placeholders", severity=SEVERITY_ERROR, message="e"
        ),
        VerificationDiagnostic(
            code="VFY_E004", check="overflow", severity=SEVERITY_WARNING, message="w"
        ),
    ]
    result = VerificationResult(Path("out.pdf"), diags)
    assert len(result.errors) == 1
    assert len(result.warnings) == 1


