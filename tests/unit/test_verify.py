def test_render_verify_with_verify_config_file(tmp_path):
    """--verify-config loads config from JSON and passes it to verify_pdf."""
    from typy.cli import cmd_render
    from typy.verify import VerificationResult

    md = tmp_path / "notes.md"
    md.write_text("# Hello", encoding="utf-8")
    output = tmp_path / "notes.pdf"
    vc_file = tmp_path / "vc.json"
    vc_file.write_text(
        json.dumps({"page_count": {"min_pages": 1}}), encoding="utf-8"
    )

    with patch("typy.builder.DocumentBuilder.save_pdf"):
        with patch("typy.verify.verify_pdf") as mock_verify:
            mock_verify.return_value = VerificationResult(output, [])
            cmd_render(
                template=None,
                data_file=None,
                markdown_file=md,
                output=output,
                verify=True,
                verify_config_file=vc_file,
            )

    mock_verify.assert_called_once()
    called_config = mock_verify.call_args[0][1]
    assert called_config.page_count.min_pages == 1


