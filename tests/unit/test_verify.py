def _make_pdf(*, pages: int = 1, fonts: list[str] | None = None, extra: bytes = b"") -> bytes:
    """Build a minimal fake PDF byte string for unit-testing the parsers.

    The generated bytes contain just enough structure for the individual
    check functions to parse:

    * ``/Count N`` for page-count detection.
    * ``/BaseFont /Name`` entries for font detection.
    * ``/MediaBox [0 0 612 792]`` for overflow detection.
    * Passthrough of any ``extra`` bytes (e.g. BT/ET text blocks).
    """
    parts: list[bytes] = [
        f"/Count {pages}".encode(),
        b"/MediaBox [0 0 612 792]",
    ]
    for font in fonts or []:
        parts.append(f"/BaseFont /{font}".encode())
    parts.append(extra)
    return b"\n".join(parts)


def _make_pdf_with_text(text: str) -> bytes:
    """Return a fake PDF with a BT/ET block containing *text*."""
    encoded = text.encode("latin-1", errors="replace")
    bt_block = b"BT (" + encoded + b") Tj ET"
    return _make_pdf(extra=bt_block)


def _make_pdf_with_large_page(width: float = 6000.0, height: float = 9000.0) -> bytes:
    """Return a fake PDF with an oversized MediaBox."""
    return (
        f"/Count 1\n/MediaBox [0 0 {width:.0f} {height:.0f}]"
    ).encode()


