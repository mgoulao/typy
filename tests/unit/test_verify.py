    result = VerificationResult(Path("out.pdf"), [])
    d = result.to_dict()
    assert d["pdf"] == "out.pdf"
    assert d["passed"] is True
    assert d["error_count"] == 0
    assert d["warning_count"] == 0
    assert d["diagnostics"] == []


